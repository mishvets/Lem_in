#include "libft.h"

void	ft_doublstrdel(char ***map, int y)
{
	int i;

	i = 0;
	while (i < y)
	{
		ft_strdel(&(*map)[i]);
		++i;
	}
	free(&(**map));
	*map = NULL;
}
